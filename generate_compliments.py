emojis = [
    "ü§ç", "üíñ", "‚ú®", "üå∏", "ü´∂", "üí´", "üå∑",
    "ü•∞", "üíå", "üåô", "‚òÄÔ∏è", "üíê", "üíû", "ü©∑",
    "üå∫", "üíò", "ü´†", "üß°"
]

def add_emojis(text):
    start_emoji = random.choice(emojis)
    end_emoji = random.choice(emojis)

    mode = random.choice(["start", "end", "both"])

    if mode == "start":
        return f"{start_emoji} {text}"
    elif mode == "end":
        return f"{text} {end_emoji}"
    elif mode == "both":
        return f"{start_emoji} {text} {end_emoji}"
    else:
        return text


compliments = []

while len(compliments) < 365:
    phrase = f"{random.choice(subjects)} {random.choice(verbs)} {random.choice(endings)}"
    phrase = add_emojis(phrase)

    if phrase not in compliments:
        compliments.append(phrase)

random.shuffle(compliments)

with open("compliments.json", "w", encoding="utf-8") as f:
    json.dump(compliments, f, ensure_ascii=False, indent=4)

